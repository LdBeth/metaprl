/**
 * A Matching is created by matching a Term with a Pattern.
 * It is an array of all the parts of the term.
 */

public class Matching
{
    /**
     * Here'sthe array of the parts.
     */
    Object[] parts;

    /**
     * Create it.
     */
    Matching(Object[] parts)
    {
        this.parts = parts;
    }
}

